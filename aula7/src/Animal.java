public abstract class Animal {

    protected String som;
    protected String nome;

    public String getSom() {
        return this.som;
    }

    public void setSom(String som) {
        this.som = som;
    }

    public String getNome() {
        return this.nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }
}